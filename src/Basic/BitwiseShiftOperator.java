package Basic;

public class BitwiseShiftOperator
{
    public static void main(String args[])
    {
        int a=10;
        int b=25;
        System.out.println("Value of a : "+ a +"Value of b : " +b);
        System.out.println("Value of a&b  : " +(a&b));
        System.out.println("Value of a|b : "+(a|b));
        System.out.println("Value of a^b : "+(a^b));
        System.out.println("Value of a<<2 : "+(a<<2));
        System.out.println("Value of a>>2 : "+(a>>2));
    }
}
