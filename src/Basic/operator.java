package Basic;

public class operator
{
    public static void main(String args[])
    {
        int a=10;
        int b=15;
        System.out.println("Value of a : " +a);
        System.out.println("Value of b : "+b);

        System.out.println("Value of a<=b : "+(a<=b));
        System.out.println("Value of a>=b : "+(a<=b));
        System.out.println("Value of a<b  :"+(a<b));
        System.out.println("Value of a>b  :"+(a>b));
        System.out.println("Value of a!=b :"+(a!=b));

    }
}
